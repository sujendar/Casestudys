package com.pixelTrice.elastic;

public class LongBucket {
	private Long key;
	private Long docCount;
	public Long getKey() {
		return key;
	}
	public void setKey(Long key) {
		this.key = key;
	}
	public Long getDocCount() {
		return docCount;
	}
	public void setDocCount(Long docCount) {
		this.docCount = docCount;
	}

}
